import {
  SidebarGroup,
  SidebarGroupContent,
  SidebarMenu,
  SidebarMenuButton,
  SidebarMenuItem,
} from '@/components/ui/sidebar';
import Link from 'next/link';
import { ComponentPropsWithoutRef } from 'react';
import { NavMenuItemProps } from '@/utils/menu.types';

interface ListSidebarMenuProps
  extends ComponentPropsWithoutRef<typeof SidebarGroup> {
  items: NavMenuItemProps[];
}

export function ListSidebarMenu({ items, ...props }: ListSidebarMenuProps) {
  return (
    <SidebarGroup {...props}>
      <SidebarGroupContent>
        <SidebarMenu>
          {items.map((item) => (
            <SidebarMenuItem key={item.title}>
              <SidebarMenuButton asChild size="sm">
                <Link href={item.url}>
                  <item.icon />
                  <span>{item.title}</span>
                </Link>
              </SidebarMenuButton>
            </SidebarMenuItem>
          ))}
        </SidebarMenu>
      </SidebarGroupContent>
    </SidebarGroup>
  );
}
